            <h4 class="w3-text-teal"><b>Acts</b></h4>
            <ol>
                <li>In Acts 1:8, what were the disciples charged to do?</li>

                <li>On the day of Pentecost, the disciples received the outpouring of the Spirit. What were they doing before this? For how long? We just read in John 20 that the disciples already received the Spirit. So, what is going on here?</li>

                <li>What title does Peter use when referring to the Lord in Acts 3:15? What does this title indicate?</li>

                <li>In Peter's defense to the Jews, in Acts 4:11, he refers to Christ as a stone.  What are stones used for, as implied in this verse?</li>

                <li>Acts 4:32-37 show a positive scene during the initial stage of the church-life. How can you characterize it? How could people so easily give up their possessions?</li>

                <li>Acts 5:1-11 show a contrasting, negative scene. Describe what happened, and why.</li>

                <li>What was Gamaliel's wise counsel to the Sanhedrin at the end of Acts 5?</li>

                <li>What was the problem in the church in the beginning of Acts 6? How was it solved? One of these serving ones was killed soon after. Who was it?</li>

                <li>Acts chapter 7 is the account of the stoning of Stephen, the first recorded martyr. Stephen gave a long discourse on the history of the Jews. But what was he emphasizing in his retelling? What was the last straw that caused the Jews to stone him?</li>

                <li>At the end of chapter 7 and the beginning of chapter 8, a young man is mentioned. Who is it?</li>

                <li>As a result of the persecution in Jerusalem, many of the believers were scattered, which afforded the Lord an opportunity to spread the gospel. Philip was an evangelist. Read the account in Acts 8:28-39. Why did Philip speak to the Ethiopian? In this account, there is a clear need to be able to interpret the Word of God. Where do we see this? Do you think Philip explained baptism as part of his message to the Ethiopian?</li>

                <li>Chapter 9 of Acts records the conversion of Saul. The Lord identified Himself as Jesus, the One who Saul persecuted. How did Saul persecute Jesus, since Jesus was no longer on the earth?</li>

                <li>Why do you think God had Ananias go to baptize Saul? As a side point, what does this say about who is qualified to perform baptisms?</li>

                <li>In Acts 10, we have the gospel being spoken to the Gentiles. What was the vision that Peter had in 10:9-16? What did it mean? After Peter preached to the house of Cornelius, did they receive the Spirit? Were they baptized before, or after, they received the Spirit?</li>

                <li>The beginning of Acts 11 shows the resistance that the Jewish believers showed towards Peter because he spoke to the Gentiles. What was the root of their problem? How did Peter address the problem?</li>

                <li>In Acts 12, we read of persecution towards the church based on the corrupt politics of Herod, who eventually gets what he deserves. Peter is thrown into prison, but is released by an angel. Yet the church is praying fervently for him. But when Peter comes to the door, what is their reaction? Isn't it a bit strange that when their prayer was answered, they wouldn't believe it?</li>

                <li>What does Acts 12:24 mean? It is helpful to consider this verse in its context.</li>

                <li>At the beginning of Acts 13, we have a description of the local church in Antioch. What were the demographics? How were Paul and Barnabas sent out?</li>

                <li>Paul quotes a Psalm in Acts 13:33. When was Christ begotten? See the next two verse for an explanation.</li>

                <li>Consider the account in chapters 13 and 14. What caused the people, after receiving the word from Paul gladly, to later turn on him and even stone him? Would you have tried to stone him if you were there?</li>

                <li>Chapter 15 of Acts records an important council held in Jerusalem. The big question was concerning what was needed to be saved. What did the Jewish believers contend? What did Paul's party contend? What was the final judgement of the council? Do you think it was accurate, or was it a compromise?</li>

                <li>In Acts 16:6, who forbade Paul to speak. Who forbade him to speak in verse 7? Putting it together, how many persons were forbidding Paul? Finally, why would God forbid Paul to speak the gospel?</li>

                <li>What were Paul and Silas doing in 16:25? Why? Would you?</li>

                <li>Contrast the behavior of the Thessalonians with the Bereans in Acts 17.</li>

                <li>How did Paul preach the gospel to the philosophical Athenians?</li>

                <li>Acts 18:24-19:7 shows us the erroneous teaching of Apollos. In what areas was his teaching inaccurate? How did this affect the Ephesians who he preached to? What can you realize about the critical importance of accurate teaching from this example?</li>

                <li>What were the seven sons of Sceva missing in Acts 19:14-16?</li>

                <li>Acts 20 begins the story of Paul's return to Jerusalem. He clearly knew what awaited him there. Why do you think he would go? In verse 18, we see that God has blood! Who is God in this verse?</li>

                <li>In chapter 21, Paul is met by James, who proposes something for Paul to do to make him acceptable to the Jews. What was it? Take a little time to study what Paul was being asked to do. Do you think he should have agreed to do it? In your consideration, realize that he never completed it. Maybe God didn't allow him to do it.</li>

                <li>In Acts 22, Paul makes a defense to the Jews. He was able to speak for awhile, until he came to one matter. What was it? Why were the Jews so upset by it?</li>

                <li>In Acts 23, what did Paul cleverly do to help save himself? How did God encourage him in verse 11?</li>

                <li>How did Paul frighten Felix in Acts 24:25?</li>

                <li>In Acts 25:10-12, Paul appealed to the civil authorities for his judgement; he appealed to Caesar. Why would he not have just left his fate to a religious judgement by the Jews? Consider if this matter was happening today. Should you just be passive in what happens to you? Or use what legal means are available? (Realize that many of Paul's letters were written after this time).</li>

                <li>Acts 26:19 shows us what motivated Paul. What was it?</li>

                <li>In the midst of a raging storm in Acts 27, Paul exhorts the men to cheer up! Is he just an eternal optimist?</li>

                <li>Upon coming to land in chapter 28, Paul is bitten by a snake. What happens, and what is the reaction of the local people? In all of the sufferings and trials in the past few chapters, how did Paul conduct himself? Look at Galatians 2:20, and see if this helps you to understand.</li>

                <li>This ends the books of Acts. But in a way, the book never ends. What chapter of Acts are we in today?</li>

