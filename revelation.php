            <h4 class="w3-text-teal"><b>Revelation</b></h4>
            <ol>
                <li>Since Revelation is a book of signs (1:1), symbols with spiritual significance, the interpretation of the signs can go in different directions. So, rather than ask "what does this signify", there will be fewer questions, but more pointing out significant matters for your consideration.</li>

                <li>Who wrote the book of Revelation? What does "revelation" mean? What is this book a revelation of?</li>

                <li>In Revelation 1:5, how is Jesus' second coming described?</li>

                <li>In Revelation 1:11, John is told to write to seven churches. These were actual local churches that existed at that time, but some Bible teachers realized that these seven churches can represent the progress of the church through history. As you read what is written to each of these churches in Revelation chapters 2 and 3, take note of what they are praised for, what they are rebuked for, and what the Lord is asking them to do.</li>

                <li>Revelation 1:20 defines what the seven lampstands are that John saw. Why would the churches be symbolized by a lampstand?</li>

                <li>Revelation chapter 4 shows the scene in heaven. There are four creatures described there, that similar to what Ezekiel saw in his first vision.</li>

                <li>We see a scroll in the hand of God in Revelation 5:1. Who was the one worthy to open the scroll?</li>

                <li>This scroll has seven seals, which are opened one by one. The seventh seal contains seven trumpets. The seventh trumpet contains seven bowls, or vessels. All of these represent events which will happen in the future (from the time Revelation was written). Some of the event will take place nearly concurrently with one another.</li>

                <li>Revelation chapter 6 unveils the opening of the first 6 seals. What happens in each?</li>

                <li>In Revelation chapter 7, there is a sealing of 144,000, 12,000 from each of the 12 tribes of Israel. These should be true Israelites who keep God's commandments during the great tribulation. God made a covenant to keep the Jews, and He will not break it. But, one of the original 12 tribes is not included. Which tribe is missing?</li>

                <li>In Revelation chapter 8, we have the opening of the seventh seal, which brings in the seven trumpets. Chapters 8 an 9 show what happens according to the sounding of the first six trumpets.</li>

                <li>In Revelation 10:1, who is the Angel described here? What is He going to do?</li>

                <li>In Revelation chapter 11, there are two witnesses who will testify for 1,260 days (3 and a half years, the length of the great tribulation). The Bible does not say who they will be, but based on the entire Bible, who do you think they could be? They will be killed by the beast. The beast is the anti-Christ, although this name is never given to him in the book of Revelation. Where do we find this name in the New Testament?</li>

                <li>At the end of chapter 11, we have the seventh trumpet which ends the general prophecy. Chapters 12-22 give details of the crucial events listed in the first 11 chapters.</li>

                <li>Chapter 12 gives the sign of a woman giving birth to a man-child, who the dragon intends to destroy. The dragon, of course, is Satan. Some interpreters say the woman is Mary giving birth to Jesus. A better interpretation is that the woman represents all of God's people giving birth to a stronger part, the man-child. Based on the principle of this book, the stronger ones should be the overcomers mentioned in chapters 2 and 3. This one is caught up into heaven (raptured) and this precipitates the war in heaven in which the devil is cast to the earth.</li>

                <li>Chapter 13 describes two beasts. The first one is the one we refer to as the anti-Christ. The second is his false prophet. This book does give some clues that will help us to recognize them. See how many you can find. One is listed in 13:3, indicating that he will suffer a fatal head wound (but be revived).</li>

                <li>Revelation 13:8 indicates that the number of the beast is 666. This has been popularized and totally misunderstood. It is really a code for who the beast is! Using an appropriate reference book, you will find that each Hebrew letter has a numerical value. We need to translate Greek (the language of this text) into Hebrew to find out. If you take the name Caesar Nero (in Greek), translate it into Hebrew, and add up the value of all the letters, you get a total of 666. More on this in chapter 17.</li>

                <li>In chapter 14, we have three "reapings". The first is the 144,000 of the Firstfruits to God in 14:1-5. Later we have a reaping 14-16, and another in 17-20. The last two reapings are larger and should be of the believers and the unbelievers. Generally people think of the rapture coming either before, or after, the great tribulation, because different verses indicate both of these. But, it seems there is rapture before, during, and after the great tribulation. Can you find verses, like those above, that support this view?</li>

                <li>Chapter 15 is a preparation for the seven bowls, the last seven plagues (included in the seventh trumpet). The seven plagues are listed in chapter 16. What are they?</li>

                <li>The first half of chapter 17 describes the judgment on the great harlot, named Babylon the great. This is generally ascribed to be apostate Christendom, the false believers and the apostate church. This is the spiritual aspect of Babylon (the name means "confusion"). The physical aspect of Babylon is also judged in chapter 18. This is the city of Rome which will be destroyed. Can you see why it is Rome from clues in the verses?</li>

                <li>The last half of chapter 17 gives more clues about anti-Christ. Verse 10 says there are seven kings, who would be seven rulers of Rome. At the time this was written, 5 Caesars had died unnaturally. One was currently Caesar (Domitian who also would be murdered). This was the sixth. The seventh had not yet come and would remain only a short time, this one will be the anti-Christ. Verse 11 indicates that the seventh will be the eighth. Putting this all together: the number 666 indicates that anti-Christ is Caesar Nero (the fifth). The seventh suffers a death wound, but will be miraculously revived, but with the spirit of Nero coming up out of the abyss. So, the seventh dies, is revived with the spirit of one who is out of the seven (Nero) and becomes the eighth.</li>

                <li>Chapter 19 nears the end of this age. We begin with a reference to the marriage dinner of the Lamb. Who is the Lamb, and who will He marry?</li>

                <li>Finally, the last half of chapter 19 describes the battle of armageddon, the final battle at the Lord's return.</li>

                <li>Chapter 20 describes in some detail what transpires after the Lord's return. This is a segment of time that is often overlooked, the millennial (1,000 year) kingdom where Christ and His overcoming believers rule and reign over the earth for 1,000 years. Even the judgment on Satan is not final, because he is bound, cast into the abyss, but loosed to lead one final rebellion. After this, he is cast into the lake of fire (our common concept of hell) for eternity. After this, there is a final judgment at the great white throne of the dead according to their works.</li>

                <li>In chapters 21 and 22, what is described is what will happen in eternity future. There will be a new heaven and new earth, everyting will be new (21:1). We also see the bride of the lamb in 21:2 and 21:9-10. But what is seen is the holy city, the New Jerusalem. How can you reconcile the bride, the wife of the Lamb being the Holy City? The description of the city is commonly used as a description of heaven, but it is clear that the Holy City descends out of heaven. With these last two chapters in view, where will we be spending eternity?</li>

