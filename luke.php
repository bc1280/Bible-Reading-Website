            <ol>
                <li>The Gospel of Luke stresses the Lord's humanity; Jesus was a genuine man. (But also born of the Holy Spirit). See this in Luke chapter 1.</li>

                <li>What city was Jesus born in? Why is He called Jesus of Nazareth? Why did this give the Pharisees a problem? (This is a little tough. You need to consider verses later on).</li>

                <li>Compare the genealogy in Luke 3 with the genealogy in Matthew 1. What differences do you see? Who is the oldest (human) ancestor in each genealogy? Why?</li>

                <li>Luke 4:16-21 is a key passage in Luke. What is the year of the Lord (the year of Jubilee)? See Leviticus 25:8-17.</li>

                <li>In the story in Luke 5:27-32, which of the groups of people do you think you fall into?</li>

                <li>In Luke 6:1-11, the Lord seemingly broke the Sabbath regulations. Why do you think He did this? What was the reaction of the scribes and Pharisees?</li>

                <li>Luke 6:37-38 shows us how we should consider others. What benefits will we get if we do this?</li>

                <li>The story in Luke 7:11-17 is only found in Luke's gospel. The gospel of Luke emphasizes the Lord's humanity. How can you see that in this story?</li>

                <li>Luke 7:47 give us a clue on how to love the Lord more. What is it?</li>

                <li>Some make the assertion that Mary is not only the mother of Jesus, but even the mother of God. But  Luke 8:21 shows us how we can be the mother and brothers of Christ.</li>

                <li>The disciples had some arguments and opinions in Luke 9:46-56 as to who is the greatest, and how to deal with ones that don't follow them. How did the Lord adjust their thinking?</li>

                <li>In Luke 10, we have the parable of the good Samaritan. This is also only found in Luke. In the story told by the Lord to the lawyer, who is the one who was robbed, who is the Samaritan, and what is the Inn?</li>

                <li>In the example of prayer given in Luke 11:3-13, what principles of prayer can you identify?</li>

                <li>How does the Lord deal with love of riches in Luke 12:13-34?</li>

                <li>Discuss what the Lord says concerning salvation in Luke 13:22-30. How can someone who ate in the presence of the Lord and was taught by Him be left out?</li>

                <li>In Luke 14:25-35, the Lord speaks concerning the cost in following Him. It seems difficult, and even harsh, especially in verse 26. What is the Lord saying to you?</li>

                <li>In the parable of the “prodigal son” in Luke 15, what was the son's primary need? What did he do to meet the need? What did the father do? Who does the older son represent?</li>

                <li>Luke 16 deals a lot with our handling of money. There is a story (note that it is not a parable) from verses 19-31. Consider the implications of this story, including: were either Lazarus or the rich man saved? What happens when we die? What will cause others to repent and believe? (Sorry, this is a very broad and deep passage. Don't dwell too much on it if you don't have time).</li>

                <li>We see some hints regarding what the age will be like when the Lord returns in Luke 17. What will the age be like?</li>

                <li>Luke 18:1-8 gives us a secret to effective prayer. What is it?</li>

                <li>In Luke 18:9-14, only one of the two men was forgiven. Which one, and why?</li>

                <li>The story of Zaccheus in Luke 19 is unique to this gospel. Compare the reaction of Zaccheus to the rich ruler in Luke 18.</li>

                <li>The Lord is examined by various parties in Luke 20. How does he defeat the Sadducees in verses 27-28?</li>

                <li>How can the giving of two coins exceed the giving of great riches? See Luke 21:1-4. Apply it to your own situation.</li>

                <li>Luke 22 covers the Lord's initiation of His table, and his arrest. In this account, Simon Peter is his natural impetuous self. What does the Lord indicate to him in verses 31-32. What is Simon's (natural) response. Eventually, did he succeed in his declaration to the Lord? Why or why not?</li>

                <li>In the account of the Lord's crucifixion in Luke 23, one of the criminals believes and is saved. What does he tell the criminal? Where did the Lord go when he died? (cf. Luke 16:19-31).</li>

                <li>After the Lord is resurrected in Luke 24, he appears to 2 of His disciples. Why didn't they recognize Him? What caused them to eventually realize Who He was?</li>

                <li>Was the Lord only a spirit in resurrection? See Luke 24:39-43 for an answer.</li>
