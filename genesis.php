            <h4 class="w3-text-teal"><b>Genesis</b></h4>
            <p style="font-size: 18px;"><b>Intro</b>: The book of Genesis is the first book of the Bible and is full of familiar Bible stories and characters. Since it is the first book, there are many thoughts and matters that are introduced. When something is mentioned for the first time in the Bible, it is like a sowing a seed that grows throughout the Holy Scriptures. See how many such items you can find as you read through the book.</p>
            <p style="font-size: 18px;">Genesis also introduces the line of Life and the line of Death. See where these lines begin, what are their principles, and follow their development (they run through the entire Bible).</p>
            <p style="font-size: 18px;">These questions are intended to help you dig into the Word of God. They are not the only questions that can be asked. If you are studying in a group, you can consider these questions, but you should also come up with other questions among all of you. Do not feel obligated to answer all of the questions. Let the Lord lead you as you study and let Him open His Word to you.</p>
            <ol>
                <li>Genesis chapter 1 is the account of creation. Trace the progression of this account, especially when life first appears. What was the last thing that God created? Why do you think this is so?</li>

                <li>When man is created in 1:26, how is he created in a different way from all the other living things. What does God charge him to do?</li>

                <li>Genesis chapter 2 is not another account of creation, but rather a description of the procedures God used to fulfill His purpose. In 2:8-9, there is a description of the garden of Eden. How many different trees are mentioned there? What are they?</li>

                <li>How did God make a counterpart for man, in 2:21-23? Compare this with what happened to Christ on the cross.</li>

                <li>In Genesis chapter 3, we have the first appearance of Satan. What is the question he asked to cause the woman to question God? Which tree did she eat of? Which tree were they then prevented from eating of?</li>

                <li>Genesis 3:15 can be viewed as the first gospel message in the Bible. What is being foretold here?</li>

                <li>What was Cain's motivation for killing Abel in Genesis 4?</li>

                <li>Trace the development of a culture independent of God, as seen in Cain's descendants. Pay attention to Enoch, Jabal, Jubal and Tubal-cain.</li>

                <li>What was the name of Seth's son  at the end of chapter 4? What does it mean? Why is that related to calling on the name of Jehovah?</li>

                <li>Genesis 5 is a genealogy. Among those listed, who lived the longest? What does his name mean? What event happened the year he died? What happened to Enoch?</li>

                <li>Genesis chapter 6 begins the story of Noah. Why was God forced to wipe out mankind? (see especially verse 4). Why did God preserve Noah?</li>

                <li>How old was Noah when he began to build the ark? How long did it take them to build it?</li>

                <li>How many of each animal did they put on the ark?</li>

                <li>How long was it before the ark came to rest? How long was it before they left the ark?</li>

                <li>What did God promise to Noah in 9:11? What was the sign of this covenant between God and man?</li>

                <li>At the end of chapter 9, Noah had a big failure. What was it? But what was the bigger failure on the part of Ham?</li>

                <li>What was the motivation behind building the tower at Babel. What does the word, "Babel" mean? Why did God come in to deal with that situation?</li>

                <li>The end of chapter 11 brings the introduction of Abram (later changed to Abraham). Why is he the "Father of the called race"? Who are his descendants?</li>

                <li>What did God promise to Abram in chapter 12?</li>

                <li>When there was a famine in chapter 12, where did Abram and his wife Sarai go? Once they were there, what did he do to preserve his life? Abraham is called the father of faith. Do you think he displayed much faith in God in this story?</li>

                <li>Why did Lot (Abram's nephew) separate himself from Abram? What did this lead to for Lot? Would it have been better for him to remain with Abram?</li>

                <li>In chapter 14, Lot is captured and Abram goes to rescue him. In verse 18, we are introduced to a very mysterious person, Melchizedek. Who was he? He was a real person, but he was also typifies Christ. In what ways? (hint: go to Hebrews chapters 5-7)</li>

                <li>Abram and Sarai were too old to have children. What did God promise him in chapter 15? When Abram believed God, what was that belief considered to Abram?</li>

                <li>In chapter 15, God also promises the land to Abram. What does God do to confirm his promise?</li>

                <li>What can we say concerning God's sovereign arrangement in Genesis 15:16?</li>

                <li>What was Sarai's bright idea to help Abram have offspring in Genesis chapter 16? How well did it work out? What was God's response? How old was Abram at that time?</li>

                <li>At the beginning of chapter 17, we see that Abram is now 99 years old. How long of a time elapsed after the birth of Ishmael? Do you think God appeared to Abram during those years?</li>

                <li>In chapter 17, what did God change Abram and Sarai's names to? What do these names mean?</li>

                <li>God promised Abraham a son by Sarah. What does this say not only about what God is able to do, but also about what regard He has for our own self-effort? (consider how old He let them become before promising a son).</li>

                <li>Chapter 18 is a very sweet story. Abraham invited three men to eat with him. Who were they? During the meal, what happened with Sarah? After the meal, what plan did they let Abraham in on? What did Abraham do in response?</li>

                <li>Chapter 19 is a very sour story. How could Lot have ended up in Sodom, the most wicked place? What happened when the two angels came to rescue them?</li>

                <li>Lots' wife became a pillar of salt. What is the significance of this? Who did Lot bear by incest to his two daughters? Who were their descendants?</li>

                <li>In chapter 30, we have a seeming repeat of an earlier failure by Abraham. What happened?</li>

                <li>We see the birth and growth of Isaac in chapter 21. What does the name Isaac mean? If Abraham typifies God the father, who does Isaac typify?</li>

                <li>Abraham offers up Isaac as a sacrifice in chapter 22. Discuss the similarities with this sacrifice and Christ's being offered. Where is Mount Moriah? Since God had promised that Isaac would be the father of a great nation, and since he did not have any children, and since Abraham was going to kill him, what does this say about Abraham's belief in resurrection?</li>

                <li>Isaac gets married in chapter 24. How does this come about? What persuaded Rebekah to go with the servant to marry someone she had never seen? Does this have any application for us?</li>

                <li>In chapter 25, how old is Isaac? When his wife conceived, she had twins. Who were they? What was unusual about the older serving the younger? What were their names and what are the meanings of their names? Which one did Isaac love? Which one did Rebekah love?</li>

                <li>How did Jacob obtain the birthright from Esau? What does this say about Esau's valuation of the birthright?</li>

                <li>In chapter 26, the phrase, "like father, like son" comes to mind. What happened?</li>

                <li>How did Rebekah and Jacob conspire to cheat Esau out of his father's blessing n chapter 27?</li>

                <li>In chapter 28, Jacob fled to his mother's family. While on the journey, he had a dream. What was the dream? Compare this account with John 1:51.</li>

                <li>Once he woke up, Jacob said this is the house of God, and the gate of heaven. Why would he call that place in the wilderness such things? What does that name Bethel mean? And why did he say that the stone he used as a pillow would become God's house? (Note: this chapter is very deep in significance).</li>

                <li>Jacob was a wily person. As a result, he went through many dealings under the hand of God. We see the beginning of these dealings in chapter 29, under the hand of his uncle, Laban. How did he deceive Jacob regarding his wives?</li>

                <li>With his wives and concubines, Jacob had 11 sons and one daughter (he would have one more later). Which one was his most beloved?</li>

                <li>How did Jacob trick Laban to obtain so many flocks? How did he get away?</li>

                <li>In chapter 32, Jacob is returning to his homeland. He knew he would encounter Esau, and was afraid Esau might kill him. What did he do to protect himself?</li>

                <li>Who did Jacob wrestle with all night? Who won? (Consider this! Jacob wrestled with God, and God did not prevail). What did the man do to Jacob?</li>

                <li>What was Jacob's name changed to? What is the meaning of it?</li>

                <li>When Jacob finally met Esau, what happened?</li>

                <li>Chapter 34 shows a further dealing that Jacob had to endure. What happened, and what did it reveal about the nature of some of Jacob's sons?</li>

                <li>In chapter 35, Jacob returns to the place where he had the dream. He built an altar, and called the name of the place El-bethel. What does that mean? When God appeared to him, how does He describe Himself?</li>

                <li>When Rachel died in childbirth, what did she name her son, and what name did Jacob change it to?</li>

                <li>In chapter 37, the story of Joseph begins. Keep in mind that Joseph typifies Christ and look for similarities between these two in all the events of Joseph's life. What were his dreams and what did they mean? What did his brothers do to him? Where did Joseph end up?</li>

                <li>Chapter 38 shows Judah indulging his lust. But through this act, who was born that became a forefather of Jesus? How does this story contrast with the way Joseph conducted himself in chapter 39? Was Joseph guilty of any crime?</li>

                <li>Joseph interpreted the dreams of two fellow prisoners. What was the outcome? (Note: how many criminals was Jesus crucified with and how many were saved?)</li>

                <li>What were Pharaoh's dreams in chapter 41, and how did Joseph interpret them? What did Pharaoh do for Joseph after his interpretation?</li>

                <li>Chapters 42-45 show Joseph's dealings with his brothers when they came to Egypt to get food. Why do you think he did not let them know who he was? What was Jacob's reaction when he sent Benjamin to Egypt (43:13-14)?</li>

                <li>In chapter 45 Joseph reveals who he is to his brothers. What did God say to Jacob to encourage him in 46:2-4?</li>

                <li>In chapter 47, Jacob stands before Pharaoh and blesses him. Since the greater blesses the lesser, what can we conclude from this very meaningful event?</li>

                <li>The famine continued for seven years, and in chapter 47, what did the people proceed to give up to get food? Who was the one distributing the food, and collecting the livelihood of the people? Who really was practically ruling over Egypt?</li>

                <li>When Jacob took his sons to be blessed by Jacob, who was the older son? Who received the superior blessing? Jacob was blind at this time, so did he make a mistake as to which son to give the top blessing to?</li>

                <li>Chapter 49 contains the blessings that Jacob gave to his own sons. Who lost the top blessing by sleeping with Jacob's concubine? What did Jaocb say about Simeon and Levi? Which son's lineage would inherit the ruling of the nation? From which son did Jesus descend?</li>

                <li>Chapter 50 of Genesis is the last chapter. This book began with God's creation for the purpose of gaining mankind to be His expression and to bear His authority on earth. How does Genesis end? Did God accomplish His purpose, or does this book end on a low note?</li>


            </ol>
